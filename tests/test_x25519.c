#include "../src/crypto/x25519.h"
#include "../src/crypto/hmac.h"
#include "vision/platform.h"

int test_x25519(void) {
    static const u8 alice_priv[32] = {
        0x77,0x07,0x6d,0x0a, 0x73,0x18,0xa5,0x7d,
        0x3c,0x16,0xc1,0x72, 0x51,0xb2,0x66,0x45,
        0xdf,0x4c,0x2f,0x87, 0xeb,0xc0,0x99,0x2a,
        0xb1,0x77,0xfb,0xa5, 0x1d,0xb9,0x2c,0x2a,
    };
    static const u8 alice_pub_expected[32] = {
        0x85,0x20,0xf0,0x09, 0x89,0x30,0xa7,0x54,
        0x74,0x8b,0x7d,0xdc, 0xb4,0x3e,0xf7,0x5a,
        0x0d,0xbf,0x3a,0x0d, 0x26,0x38,0x1a,0xf4,
        0xeb,0xa4,0xa9,0x8e, 0xaa,0x9b,0x4e,0x6a,
    };
    static const u8 bob_priv[32] = {
        0x5d,0xab,0x08,0x7e, 0x62,0x4a,0x8a,0x4b,
        0x79,0xe1,0x7f,0x8b, 0x83,0x80,0x0e,0xe6,
        0x6f,0x3b,0xb1,0x29, 0x26,0x18,0xb6,0xfd,
        0x1c,0x2f,0x8b,0x27, 0xff,0x88,0xe0,0xeb,
    };
    static const u8 bob_pub_expected[32] = {
        0xde,0x9e,0xdb,0x7d, 0x7b,0x7d,0xc1,0xb4,
        0xd3,0x5b,0x61,0xc2, 0xec,0xe4,0x35,0x37,
        0x3f,0x83,0x43,0xc8, 0x5b,0x78,0x67,0x4d,
        0xad,0xfc,0x7e,0x14, 0x6f,0x88,0x2b,0x4f,
    };
    static const u8 shared_expected[32] = {
        0x4a,0x5d,0x9d,0x5b, 0xa4,0xce,0x2d,0xe1,
        0x72,0x8e,0x3b,0xf4, 0x80,0x35,0x0f,0x25,
        0xe0,0x7e,0x21,0xc9, 0x47,0xd1,0x9e,0x33,
        0x76,0xf0,0x9b,0x3c, 0x1e,0x16,0x17,0x42,
    };

    u8 alice_pub[32], bob_pub[32], shared_a[32], shared_b[32];

    vision_x25519_pubkey(alice_priv, alice_pub);
    if (vision_memcmp(alice_pub, alice_pub_expected, 32) != 0) return 1;

    vision_x25519_pubkey(bob_priv, bob_pub);
    if (vision_memcmp(bob_pub, bob_pub_expected, 32) != 0) return 2;

    if (vision_x25519(alice_priv, bob_pub, shared_a) != 0) return 3;
    if (vision_memcmp(shared_a, shared_expected, 32) != 0) return 4;

    if (vision_x25519(bob_priv, alice_pub, shared_b) != 0) return 5;
    if (vision_memcmp(shared_a, shared_b, 32) != 0) return 6;

    u8 low_order[32]; vision_memset(low_order, 0, 32);
    u8 dummy[32];
    if (vision_x25519(alice_priv, low_order, dummy) != -1) return 7;

    static const u8 ikm[]  = { 0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,
                                0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,0x0b,
                                0x0b,0x0b,0x0b,0x0b,0x0b,0x0b };
    static const u8 salt[] = { 0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
                                0x08,0x09,0x0a,0x0b,0x0c };
    static const u8 info[] = { 0xf0,0xf1,0xf2,0xf3,0xf4,0xf5,0xf6,0xf7,
                                0xf8,0xf9 };
    static const u8 prk_expected[32] = {
        0x07,0x77,0x09,0x36, 0x2c,0x2e,0x32,0xdf,
        0x0d,0xdc,0x3f,0x0d, 0xc4,0x7b,0xba,0x63,
        0x90,0xb6,0xc7,0x3b, 0xb5,0x0f,0x9c,0x31,
        0x22,0xec,0x84,0x4a, 0xd7,0xc2,0xb3,0xe5,
    };
    static const u8 okm_expected[42] = {
        0x3c,0xb2,0x5f,0x25, 0xfa,0xac,0xd5,0x7a,
        0x90,0x43,0x4f,0x64, 0xd0,0x36,0x2f,0x2a,
        0x2d,0x2d,0x0a,0x90, 0xcf,0x1a,0x5a,0x4c,
        0x5d,0xb0,0x2d,0x56, 0xec,0xc4,0xc5,0xbf,
        0x34,0x00,0x72,0x08, 0xd5,0xb8,0x87,0x18,
        0x58,0x65,
    };

    u8 prk[32];
    vision_hkdf_extract(salt, sizeof(salt), ikm, sizeof(ikm), prk);
    if (vision_memcmp(prk, prk_expected, 32) != 0) return 10;

    u8 okm[42];
    if (vision_hkdf_expand(prk, 32, info, sizeof(info), okm, 42) != 0) return 11;
    if (vision_memcmp(okm, okm_expected, 42) != 0) return 12;

    return 0;
}
